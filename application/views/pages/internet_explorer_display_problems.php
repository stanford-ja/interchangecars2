<h3>Internet Explorer Display Problems</h3>
This application may display incorrectly in Internet Explorer if IE settings are set incorrectly for this application. 
During testing of the application, it was found that this application works best for Internet Explorer when the following applies:
<ul>
	<li>Internet Explorer 9 or above.</li>
	<li>Document Mode: Internet Explorer 9 standards mode (NOT quircks mode!).</li>
</ul>
To set Internet Explorer to use IE9 Standards Mode, do the following:
<ul>
	<li>Nagivate to the home page of this application (click the Home link in the menu).</li>
	<li>If IE Menu is not already displayed above the application (in or near the toolbars area), then right-click the mouse in the Internet Explorer Title bar, then click the Menu Bar option.</li>
	<li>In the IE Menu.</li>
	<li>Click the Tools option, then F12 Developers Tools.</li>
	<li>In the menu that appears click Document Mode.</li>
	<li>Select Internet Explorer 9 standards.</li>
	<li>Click File, then Exit.</li>
</ul>
